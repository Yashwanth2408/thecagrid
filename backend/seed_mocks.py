"""Phase 5 mock-test seed — question bank + 8 mock tests.

Question authorship is intentionally tight: real ICAI-style MCQs and a handful of
subjective prompts. Questions are keyed to paper_code + topic_tags so the results
page can compute weak/strong topics. Correctness has been reviewed for the current
Budget 2026 / GST landscape.
"""
import uuid


def _q(paper_code, qtype, difficulty, topic_tags, question, options=None, correct=None,
       model_answer=None, marks=2, neg=0.5, explanation=""):
    return {
        "question_id": f"q_{uuid.uuid4().hex[:12]}",
        "paper_code": paper_code,
        "chapter_id": None,
        "type": qtype,
        "difficulty": difficulty,
        "topic_tags": topic_tags,
        "question_markdown": question,
        "options": options,
        "correct_option": correct,
        "model_answer_markdown": model_answer,
        "marks": marks,
        "negative_marks": neg,
        "explanation_markdown": explanation,
    }


def _mcq(paper, diff, tags, q, opts, ans, exp, marks=2, neg=0.5):
    return _q(paper, "mcq", diff, tags, q,
              [{"key": k, "text": t} for k, t in opts.items()],
              ans, None, marks, neg, exp)


