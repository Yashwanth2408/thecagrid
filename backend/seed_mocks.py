def _pick_ids(questions, n):
    """Return question_ids of first n. If fewer available, cycles to reach n."""
    ids = [q["question_id"] for q in questions]
    if len(ids) >= n:
        return ids[:n]
    # If insufficient, repeat (allowed in seed since it's illustrative)
    out = []
    i = 0
    while len(out) < n:
        out.append(ids[i % len(ids)])
        i += 1
    return out


